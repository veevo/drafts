# Porque não temos notícias dos nossos parentes que partiram?

Chico dá uma explicação:

As lentes Mediúnicas. 

Certa vez, perguntei ao medianeiro:
-Chico, eu tinha um tio, irmão mais velho de meu pai, muito bondoso e atencioso conosco. Quase que diariamente passava na casa de meu pai e lá ele permanecia alguns minutos, perguntava por todos e logo partia. 
Deixou o corpo físico em 1957. Logo após a sua passagem, recebemos algumas notícias dele, mas, depois, não tivemos mais nenhum contato e não sentimos mais sua presença. Gostaríamos de saber o porquê dessa falta de comunicação com ele. 
O médium ouviu-me, ponderou minhas palavras e respondeu: 

-Romeu, tanto ele como espíritos de familiares e amigos afins estão ai, junto de nós, mas não são vistos, e a presença deles sequer é sentida. Faltam lentes mediúnicas para detecta-los e possibilitar a comunicação com eles. 

Fiquei a meditar na resposta do amigo Chico e acabei por entender que as lentes mediúnicas mencionadas consistem em um maior potencial mediúnico, presente em raros médiuns, sendo ele próprio um deles. 

